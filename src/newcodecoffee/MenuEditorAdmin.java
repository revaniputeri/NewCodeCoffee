    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            Statement stat = konek.createStatement();
            ResultSet data = stat.executeQuery("select*from menu where kode_menu ='"+ jTextField1.getText()+ "'");
            if (data.next()) //jika data ditemukan
            {
                //menampilkan pesan bahwa data menu sudah ada
                JOptionPane.showMessageDialog(null,"Kode Menu sudah ada!!!", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
                jTextField1.requestFocus();
            } 
            else if(jTextField1.getText().equals("")||jTextField2.getText().equals("")
                    ||jTextField3.getText().equals("")) //jika isisan txtfield kosong
            {
                JOptionPane.showMessageDialog(null,"Data tidak boleh kosong!!!", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
            }
            
            else
            {
                String sql = "INSERT INTO menu VALUES('"+jTextField1.getText() + "'"
                        +",'" + jTextField2.getText()+ "'"
                        +",'" + jTextField3.getText()+ "')";
                 stat.executeUpdate(sql);
                 
                 jTextField1.setText("");
                 jTextField2.setText("");
                 jTextField3.setText("");
                 jTextField1.requestFocus();
                 
                 JOptionPane.showMessageDialog(null, "Data berhasil disimpan!!!","SUKSES",JOptionPane.INFORMATION_MESSAGE);
                 tampilData();
            }
            stat.close();
        }
        catch (Exception exc)
        {
            System.err.println("Terjadi Kesalahan:" +exc);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

