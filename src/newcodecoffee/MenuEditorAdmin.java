private void cariData(String key) throws SQLException{
    try{
        Object[] judul_kolom = {"Kode","Nama Menu", "Harga Menu"};
        tabModel= new DefaultTableModel(null, judul_kolom);
        jTable1.setModel(tabModel);
        
        Connection conn = (Connection)Koneksi.koneksiDb();
        Statement stat = conn.createStatement();
        tabModel.getDataVector().removeAllElements();
        
        RsProduk = stat.executeQuery("select * from menu where kode_menu like '%"+key+"%' "+"or nama_menu like '%"+key+"%' "+" or harga_menu like '%"+key+"%'");
        while(RsProduk.next()){
            Object[] data = {
                RsProduk.getString ("kode_menu"),
                RsProduk.getString ("nama_menu"),
                RsProduk.getString ("harga_menu"),
            };
            tabModel.addRow(data);
        }
    } catch (Exception ex){
        System.err.println(ex.getMessage());
    }
}

